/* global io */
/* global app */
/* global factory */
app.factory('socketFactory', ['$rootScope','configurationService',function ($rootScope,configurationService){
  var socket = io.connect(configurationService.urlSocketServer);
  return {
    on: function (eventName, callback) {
      socket.on(eventName, function () {  
        var args = arguments;
        $rootScope.$apply(function () {
          callback.apply(socket, args);
        });
      });
    },
    emit: function (eventName, data, callback) {
      socket.emit(eventName, data, function () {
        var args = arguments;
        $rootScope.$apply(function () {
          if (callback) {
            callback.apply(socket, args);
          }
        });
      })
    }
  };
}]);